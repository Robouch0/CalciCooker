function getOrderSprite(name) {
    if (name == "nomnom") {
        return nomnomSprite;
    }
    if (name == "hauru") {
        return hauruSprite;
    }
    if (name == "normal") {
        return normalSprite;
    }
    if (name == "sausageo") {
        return sausageoSprite;
    }
    if (name == "empty") {
        return emptyOrderSprite;
    }
}
